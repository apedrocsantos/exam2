#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
			i++;
		i--;
		while (argv[1][i] && (argv[1][i] == ' ' || argv[1][i] == '\t'))
			i--;
		j = i;
		while (argv[1][j] && !(argv[1][j] == ' ' || argv[1][j] == '\t'))
			j--;
		while (j++ < i)
			write(1, &argv[1][j], 1);
	}
	write(1, "\n", 1);
}
