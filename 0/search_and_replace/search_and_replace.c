#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;

	i = 0;
	if (argc == 4 && !argv[2][1] && !argv[3][1])
	{
		while (argv[1][i])
		{
			if (argv[1][i] == *argv[2])
				argv[1][i] = *argv[3];
			write(1, &argv[1][i++], 1);
		}
	}
	write(1, "\n", 1);
}
